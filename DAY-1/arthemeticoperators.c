#include<stdio.h>
int main() {
    float a=20;
    float b=10;

    float sum = a + b;
    float difference = a - b;
    float product = a * b;
    float division = a / b;

    printf("Sum: %.2f\n", sum);
    printf("Difference: %.2f\n", difference);
    printf("Product: %.2f\n", product);
    printf("Division: %.2f\n", division);
}